package com.marto.ifstatement.examples;

public class IfStatementRunner {
  public static void main(String[] args) {
    int i = 26;
    if (i == 25) {
      System.out.println("i is 25");
    } else if (i == 24) {
      System.out.println("i is 25");
    } else {
      System.out.println("i is neither 25 nor 24");
    }
  }
}
